import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const LOCALES_DIR = path.join(__dirname, '../i18n/locales/en');
const OUTPUT_FILE = path.join(__dirname, '../i18n/types/i18n.d.ts');

function generateTypesForValue(value, indent = '  ') {
  if (value && typeof value === 'object' && !Array.isArray(value)) {
    let result = '{\n';
    for (const [k, v] of Object.entries(value)) {
      result += `${indent}  "${k}": ${generateTypesForValue(v, indent + '  ')};\n`;
    }
    result += `${indent}}`;
    return result;
  }
  return 'string';
}

async function buildTypeSafety() {
  console.log('Generating compile-time type-safety definitions for i18n...');

  if (!fs.existsSync(LOCALES_DIR)) {
    console.error(`English locales dir does not exist: ${LOCALES_DIR}`);
    process.exit(1);
  }

  const outputDir = path.dirname(OUTPUT_FILE);
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
  }

  const files = fs.readdirSync(LOCALES_DIR).filter(file => file.endsWith('.json'));
  
  let typeDefs = '// Auto-generated by scripts/generate-i18n-types.mjs. Do not edit manually.\n\n';
  const namespaces = [];

  for (const file of files) {
    const nsName = path.basename(file, '.json');
    namespaces.push(nsName);
    const content = JSON.parse(fs.readFileSync(path.join(LOCALES_DIR, file), 'utf8'));

    typeDefs += `interface ${nsName}NS {\n`;
    for (const [k, v] of Object.entries(content)) {
      typeDefs += `  "${k}": ${generateTypesForValue(v, '  ')};\n`;
    }
    typeDefs += `}\n\n`;
  }

  typeDefs += `declare module 'react-i18next' {\n`;
  typeDefs += `  interface CustomTypeOptions {\n`;
  typeDefs += `    defaultNS: 'common';\n`;
  typeDefs += `    resources: {\n`;
  for (const ns of namespaces) {
    typeDefs += `      "${ns}": ${ns}NS;\n`;
  }
  typeDefs += `    };\n`;
  typeDefs += `  }\n`;
  typeDefs += `}\n\n`;
  typeDefs += `export {};\n`;

  fs.writeFileSync(OUTPUT_FILE, typeDefs, 'utf8');
  console.log(`✅ Type definitions written to ${OUTPUT_FILE}`);
}

buildTypeSafety().catch(err => {
  console.error('Error generating i18n types:', err);
  process.exit(1);
});
